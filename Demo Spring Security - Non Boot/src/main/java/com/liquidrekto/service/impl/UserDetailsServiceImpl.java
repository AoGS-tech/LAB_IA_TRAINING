package com.liquidrekto.service.impl;

import com.liquidrekto.data.model.User;
import com.liquidrekto.data.repository.UserRepository;
import com.liquidrekto.impl.UserDetailsImpl;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

/**
 *
 * @author Admin
 */
@Service
public class UserDetailsServiceImpl implements UserDetailsService {

    @Autowired
    private UserRepository userRepository;
    
    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        User user = userRepository.findByAccountName(username);
        if (user == null)
            throw new UsernameNotFoundException("User not found");
        
        return new UserDetailsImpl(user);
    }
    
}
